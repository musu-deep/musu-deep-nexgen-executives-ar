const REGEX = [
  [/^Welcome,\s*(.+)$/i, "مرحبًا، $1"],
  [/^(\d+)\s+active$/i, "$1 نشط"],
  [/^(\d+)\s+projects?\s*•\s*Filtered by your permissions$/i, "$1 مشروعًا • وفق صلاحيات الوصول الخاصة بك"],
  [/^(\d+)\s+Important Tasks\s*•\s*(\d+)\s+Status Columns$/i, "$1 مهمة مهمة • $2 من أعمدة الحالة"],
  [/^(\d+)\s+request(?:s)?$/i, "$1 طلبًا"],
  [/^(\d+)\s+New Meeting(?:s)?$/i, "$1 اجتماعًا"],
  [/^(\d+)\s+Attendees$/i, "$1 من الحضور"],
  [/^(\d+)\s+min$/i, "$1 دقيقة"],
  [/^From:\s*(.+)$/i, "مقدم الطلب: $1"],
  [/^Duration:\s*(\d+)\s+min$/i, "المدة: $1 دقيقة"],
  [/^Note:\s*(.+)$/i, "ملاحظة: $1"],
  [/^Priority:\s*(.+)$/i, "الأولوية: $1"],
  [/^Due:\s*(.+)$/i, "الاستحقاق: $1"],
  [/^Source:\s*(.+)$/i, "المصدر: $1"],
  [/^Risk level:\s*(.+)$/i, "مستوى المخاطر: $1"],
];

function keepSpacing(original, translated) {
  const leading = original.match(/^\s*/)?.[0] || "";
  const trailing = original.match(/\s*$/)?.[0] || "";
  return `${leading}${translated}${trailing}`;
}

export function translateExtraArabicText(value) {
  if (typeof value !== "string" || !value.trim()) return value;
  const trimmed = value.trim();
  if (EXTRA[trimmed]) return keepSpacing(value, EXTRA[trimmed]);
  for (const [pattern, replacement] of REGEX) {
    pattern.lastIndex = 0;
    if (pattern.test(trimmed)) {
      pattern.lastIndex = 0;
      return keepSpacing(value, trimmed.replace(pattern, replacement));
    }
  }
  return value;
}

export default EXTRA;
